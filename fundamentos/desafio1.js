//Cretate a range function:

//range(5)->[1,2,3,4,5]

function rangeDesafio(num){
    for(var i=1; i<=num;i++){
        
        console.log(i);
    }
};

rangeDesafio(5);

//range(6,11)->[6,7,8,9,10,11]

function rangeDesafioDois(numInit,numFinal){
    for(var j=numInit;j<=numFinal;j++){
        console.log(j);
    }
};
rangeDesafioDois(6,11)
